    def _load_cover_assignment(self) -> dict[str, dict[str, str]]:
        assignments: dict[str, dict[str, str]] = {}
        payload = self._load_json(self.cover_assignment_path)
        if not isinstance(payload, list):
            return assignments

        for item in payload:
            if not isinstance(item, dict):
                continue
            slug = self._safe_text(item.get("slug"))
            if not slug:
                continue
            assignments[slug] = {
                "status": self._safe_text(item.get("status")),
                "front_url": self._safe_text(item.get("front_url")),
                "back_url": self._safe_text(item.get("back_url")),
                "source": self._safe_text(item.get("source")),
            }
        return assignments

    def _load_cover_upload_reports(self) -> dict[str, dict[str, Any]]:
        mapping: dict[str, dict[str, Any]] = {}
        for path in DEFAULT_COVER_UPLOAD_REPORTS:
            if not path.exists():
                continue
            try:
                payload = self._load_json(path)
            except Exception:
                continue
            for item in payload.get("results", []):
                if not isinstance(item, dict):
                    continue
                slug = self._safe_text(item.get("slug"))
                if not slug:
                    continue
                uploads = item.get("uploads", {})
                if not isinstance(uploads, dict):
                    uploads = {}
                uploads_payload = {
                    "front_url": self._safe_text(
                        uploads.get("front", {}).get("cover_url") if isinstance(uploads.get("front"), dict) else ""
                    ),
                    "back_url": self._safe_text(
                        uploads.get("back", {}).get("cover_url") if isinstance(uploads.get("back"), dict) else ""
                    ),
                    "front_local_path": self._safe_text(
                        uploads.get("front", {}).get("local_path") if isinstance(uploads.get("front"), dict) else ""
                    ),
                    "back_local_path": self._safe_text(
                        uploads.get("back", {}).get("local_path") if isinstance(uploads.get("back"), dict) else ""
                    ),
                }
                if uploads_payload["front_local_path"] or uploads_payload["back_local_path"] or uploads_payload["front_url"] or uploads_payload["back_url"]:
                    mapping[slug] = uploads_payload
        return mapping

    def _load_cover_slug_mapping_refined(self) -> dict[str, dict[str, Any]]:
        payload = self._load_json(DEFAULT_COVER_MAPPING_REPO)
        if not isinstance(payload, dict):
            return {}
        items = payload.get("items")
        if not isinstance(items, list):
            return {}
        mapping: dict[str, dict[str, Any]] = {}
        for item in items:
            if not isinstance(item, dict):
                continue
            slug = self._safe_text(item.get("slug"))
            if not slug:
                continue
            mapping[slug] = item
        return mapping

