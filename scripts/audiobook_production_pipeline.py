    def _find_local_cover_candidates(self, slug: str) -> tuple[str | None, str | None]:
        entry = self.cover_mapping_refined.get(slug, {})
        front_candidates = []
        back_candidates = []
        search_roots = [
            self.content_root / slug,
            self.content_root / slug / "cover",
            self.content_root / slug / "covers",
            self.content_root / slug / "media" / "covers",
        ]
        for key in ("front_files", "front"):
            for value in entry.get(key, []) if isinstance(entry.get(key), list) else [entry.get(key)] if entry.get(key) else []:
                if value:
                    front_candidates.append(Path(value))
        for key in ("back_files", "back"):
            for value in entry.get(key, []) if isinstance(entry.get(key), list) else [entry.get(key)] if entry.get(key) else []:
                if value:
                    back_candidates.append(Path(value))

        for directory in DEFAULT_COVER_OUTPUT_DIRS:
            for extension in (".png", ".jpg", ".jpeg", ".webp"):
                for side in ("front", "back"):
                    candidate = directory / f"{slug}_{side}{extension}"
                    if side == "front":
                        front_candidates.append(candidate)
                    else:
                        back_candidates.append(candidate)
        for directory in search_roots:
            if not directory.exists():
                continue
            for side in ("front", "back"):
                for extension in (".png", ".jpg", ".jpeg", ".webp"):
                    for candidate in directory.glob(f"*{slug}*{side}*{extension}"):
                        if side == "front":
                            front_candidates.append(candidate)
                        else:
                            back_candidates.append(candidate)
                    for candidate in directory.glob(f"*{side}*cover*{extension}"):
                        if side == "front":
                            front_candidates.append(candidate)
                        else:
                            back_candidates.append(candidate)
                    for candidate in directory.glob(f"**/*{side}*{extension}"):
                        if side == "front":
                            front_candidates.append(candidate)
                        else:
                            back_candidates.append(candidate)
        front_path: str | None = None
        back_path: str | None = None
        for candidate in front_candidates:
            path = candidate
            if not candidate.is_absolute():
                path = ROOT / candidate
            if path.exists() and path.suffix.lower() in {".png", ".jpg", ".jpeg", ".webp"}:
                front_path = str(path)
                break
        for candidate in back_candidates:
            path = candidate
            if not candidate.is_absolute():
                path = ROOT / candidate
            if path.exists() and path.suffix.lower() in {".png", ".jpg", ".jpeg", ".webp"}:
                back_path = str(path)
                break
        return front_path, back_path

    def _parse_cover_upload_response(self, payload: Any) -> dict[str, str]:
        if not isinstance(payload, dict):
            return {}
        result: dict[str, str] = {}
        for key in (
            "cover_url",
            "frontCoverUrl",
            "backCoverUrl",
            "url",
            "location",
            "secure_url",
        ):
            value = self._safe_text(payload.get(key))
            if value:
                result[key] = value
        for key in ("cover", "asset", "file"):
            nested = payload.get(key)
            if isinstance(nested, dict):
                value = self._safe_text(nested.get("url") or nested.get("secure_url"))
                if value:
                    result[key] = value
        return result

    def _cover_upload_session(self) -> requests.Session | None:
        token = os.getenv("EARNALISM_ADMIN_TOKEN")
        if token is None or not str(token).strip():
            email = self._safe_text(os.getenv("ADMIN_EMAIL"))
            password = self._safe_text(os.getenv("ADMIN_PASSWORD"))
            if not email or not password:
                return None
            api_url = self._safe_text(os.getenv("EARNALISM_API_URL") or os.getenv("API_URL"))
            if not api_url:
                return None
            try:
                session = requests.Session()
                response = session.post(
                    f"{api_url.rstrip('/')}/api/auth/login",
                    json={"email": email, "password": password},
                    timeout=30,
                )
                response.raise_for_status()
                payload = response.json() if response.text else {}
                bearer = self._safe_text(payload.get("token"))
                if not bearer:
                    return None
                session.headers.update({"Authorization": f"Bearer {bearer}"})
                return session
            except Exception:
                return None
        session = requests.Session()
        session.headers.update({"Authorization": f"Bearer {token}"})
        return session

    def _api_url(self) -> str:
        return self._safe_text(
            os.getenv("EARNALISM_API_URL") or os.getenv("API_URL") or "https://api.theearnalism.com"
        )

    def _upload_cover_file(self, slug: str, local_path: Path, side: str) -> tuple[bool, str | None]:
        session = self._cover_upload_session()
        if session is None:
            return False, "admin credentials unavailable for cover upload"
        url = self._api_url()
        if not self._safe_text(url):
            return False, "API URL unavailable for cover upload"
        if not local_path.exists():
            return False, f"local cover file missing: {local_path}"
        endpoint = f"{url.rstrip('/')}/api/admin/books/{slug}/cover"
        try:
            with local_path.open("rb") as handle:
                response = session.post(
                    endpoint,
                    params={"kind": side},
                    files={"file": (local_path.name, handle, "image/png")},
                    timeout=120,
                )
            response.raise_for_status()
        except Exception as exc:
            return False, f"{side} upload request failed: {exc}"

        payload = {}
        try:
            payload = response.json()
        except Exception:
            text = self._safe_text(response.text)
            if text:
                payload = {"url": text}
        parsed = self._parse_cover_upload_response(payload)
        url_value = None
        for value in parsed.values():
            if value:
                url_value = value
                break
        if not url_value:
            return True, None
        return True, url_value

    def _resolve_cover_urls(self, slug: str, candidate: dict[str, Any], result: TitleResult) -> tuple[str, str]:
        front_url = ""
        back_url = ""
        source_details: list[str] = []
        book_json = self._load_json(self.content_root / slug / "book.json")
        mapping_item = self.cover_mapping_refined.get(slug, {})
        result.cover_assignment_source = "unresolved"
        result.cover_assignment_checked = False
        result.gate0_cover_assets_missing = False

        def _register_if_conflict(url: str, side: str) -> str:
            normalized = _normalize_pattern_path(url)
            if not normalized:
                return normalized
            prior_slug = self.cover_url_assignments.get(normalized)
            if prior_slug and prior_slug != slug:
                result.gate0_cover_url_inconsistent = True
                result.add_failure(
                    f"Cover URL reuse conflict for {side}: {url} is already assigned to {prior_slug}"
                )
            else:
                self.cover_url_assignments[normalized] = slug
            return normalized

        def _set_if_cloud(url: str, target: str) -> None:
            nonlocal front_url, back_url
            if not url:
                return
            if not self._is_cloudinary_url(url):
                result.sanitation_warnings.append(f"Non-cloudinary cover URL for {slug} {target}: {url}")
            if target == "front":
                if not front_url:
                    front_url = url
            elif target == "back":
                if not back_url:
                    back_url = url

        assignment = self._cover_entry(slug)
        if assignment:
            front_candidate = self._safe_text(assignment.get("front_url"))
            back_candidate = self._safe_text(assignment.get("back_url"))
            if front_candidate:
                source_details.append("mapping:front")
                _set_if_cloud(front_candidate, "front")
            if back_candidate:
                source_details.append("mapping:back")
                _set_if_cloud(back_candidate, "back")

        report_entry = self.cover_upload_report_map.get(slug, {})
        if not front_url:
            front_candidate = self._safe_text(report_entry.get("front_url"))
            if front_candidate:
                source_details.append("cover_upload_report:front")
                _set_if_cloud(front_candidate, "front")
        if not back_url:
            back_candidate = self._safe_text(report_entry.get("back_url"))
            if back_candidate:
                source_details.append("cover_upload_report:back")
                _set_if_cloud(back_candidate, "back")

        if not (front_url and back_url):
            book_public = self._load_json(self.controlled_root / slug / "public_book.json")
            if isinstance(book_public, dict):
                candidate_front = self._safe_text(
                    book_public.get("cover_url")
                    or book_public.get("cover_image_url")
                    or book_public.get("front_cover_url")
                )
                candidate_back = self._safe_text(
                    book_public.get("back_cover_url")
                    or book_public.get("back_cover_image_url")
                )
                if candidate_front and not front_url:
                    _set_if_cloud(candidate_front, "front")
                    source_details.append("public_book:front")
                if candidate_back and not back_url:
                    _set_if_cloud(candidate_back, "back")
                    source_details.append("public_book:back")

        if isinstance(book_json, dict):
            cover_assets = book_json.get("coverAssets")
            if isinstance(cover_assets, list):
                if not cover_assets:
                    result.gate0_cover_assets_missing = True
                for item in cover_assets:
                    if not isinstance(item, dict):
                        continue
                    if not front_url:
                        candidate_front = self._safe_text(
                            item.get("front") or item.get("frontCover") or item.get("cover") or item.get("url")
                        )
                        if candidate_front:
                            front_url = candidate_front
                            source_details.append("book_json:frontAsset")
                            _set_if_cloud(candidate_front, "front")
                    if not back_url:
                        candidate_back = self._safe_text(
                            item.get("back") or item.get("backCover") or item.get("backUrl")
                        )
                        if candidate_back:
                            back_url = candidate_back
                            source_details.append("book_json:backAsset")
                            _set_if_cloud(candidate_back, "back")

        if not front_url or not back_url:
            local_front, local_back = self._find_local_cover_candidates(slug)
            local_front_path = Path(local_front) if local_front else None
            local_back_path = Path(local_back) if local_back else None

            if front_url:
                local_front_path = None
            if back_url:
                local_back_path = None

            if local_front_path and local_front_path.exists():
                uploaded, upload_reason = self._upload_cover_file(slug, local_front_path, "front")
                source_details.append(f"local_front:{'uploaded' if uploaded else 'upload_failed'}")
                if uploaded:
                    if upload_reason:
                        front_url = upload_reason
                        source_details.append("cover_upload:front")
                        result.sanitation_warnings.append(f"{slug} front uploaded from local file {local_front_path}")
                    else:
                        result.add_failure(f"Cover upload for {slug} front succeeded without returning a URL")
                else:
                    result.failures.append(f"front cover upload failed: {upload_reason}")

            if local_back_path and local_back_path.exists():
                uploaded, upload_reason = self._upload_cover_file(slug, local_back_path, "back")
                source_details.append(f"local_back:{'uploaded' if uploaded else 'upload_failed'}")
                if uploaded:
                    if upload_reason:
                        back_url = upload_reason
                        source_details.append("cover_upload:back")
                        result.sanitation_warnings.append(f"{slug} back uploaded from local file {local_back_path}")
                    else:
                        result.failures.append(f"Cover upload for {slug} back succeeded without returning a URL")
                else:
                    result.failures.append(f"back cover upload failed: {upload_reason}")

            if (local_front_path and local_back_path and not front_url and not back_url) and (not mapping_item):
                result.add_failure("Missing cover URL pairing from assignments and local uploads")

        if front_url and back_url:
            _register_if_conflict(front_url, "front")
            _register_if_conflict(back_url, "back")
            result.cover_front_url = front_url
            result.cover_back_url = back_url
            if source_details:
                result.sanitation_warnings.append(
                    f"Cover URLs resolved for {slug}: {' + '.join(source_details)}"
                )
            self.cover_assignment_map.setdefault(slug, {})["front_url"] = front_url
            self.cover_assignment_map.setdefault(slug, {})["back_url"] = back_url
            if not self.cover_assignment_map.get(slug, {}).get("status"):
                self.cover_assignment_map[slug]["status"] = "mapped_front_back"
        else:
            result.gate0_cover_missing = True
            result.add_failure(
                "Missing cover URL pairing (front/back) from mapping, cover upload reports, public_book.json, book JSON, or local upload"
            )
            result.cover_front_url = ""
            result.cover_back_url = ""
            if front_url or back_url:
                result.gate0_cover_missing = True
        if not source_details:
            source_details.append("unresolved")
        result.cover_assignment_source = "+".join(source_details)
        result.cover_assignment_checked = True

        return front_url, back_url

    @staticmethod
    def _safe_text(value: Any) -> str:
        return str(value or "").strip()

    @staticmethod
    def _iso_utc_now() -> str:
        return datetime.now(timezone.utc).strftime("%Y-%m-%dT%H:%M:%S%z")

    @staticmethod
    def _ensure_dir(path: Path) -> None:
        path.mkdir(parents=True, exist_ok=True)

    def _load_json(self, path: Path) -> Any:
        if not path.exists():
            return {}
        try:
            raw = path.read_text(encoding="utf-8")
            return json.loads(raw)
        except Exception as exc:
            logger.warning("Failed JSON load %s: %s", path, exc)
            return {}

    def _load_json_strict_utf8(self, path: Path) -> tuple[dict[str, Any], bool, list[str], bool]:
        if not path.exists():
            return {}, False, [f"missing file {path.relative_to(ROOT)}"], False
        try:
            raw = path.read_text(encoding="utf-8")
        except UnicodeDecodeError as exc:
            return {}, False, [f"invalid UTF-8 in {path.relative_to(ROOT)}: {exc}"], True
        except Exception as exc:
            return {}, False, [f"failed read {path.relative_to(ROOT)}: {exc}"], False

        try:
            payload = json.loads(raw)
            if not isinstance(payload, dict):
                return (
                    {},
                    True,
                    [f"invalid JSON object structure in {path.relative_to(ROOT)}"],
                    True,
                )
            return payload, True, [], True
        except Exception as exc:
            return (
                {},
                False,
                [f"invalid JSON in {path.relative_to(ROOT)}: {exc}"],
                True,
            )

    def _write_json(self, path: Path, payload: Any) -> None:
        self._ensure_dir(path.parent)
        path.write_text(json.dumps(payload, ensure_ascii=False, indent=2) + "\n", encoding="utf-8")

    def _write_text(self, path: Path, text: str) -> None:
        self._ensure_dir(path.parent)
        path.write_text((text.strip() + "\n"), encoding="utf-8")

    def _read_text(self, path: Path, errors: str = "strict") -> str:
        if not path.exists():
            return ""
        return path.read_text(encoding="utf-8", errors=errors)

    def _read_text_with_validation(self, path: Path) -> tuple[str, bool, str]:
        if not path.exists():
            return "", False, "missing"
        try:
            return path.read_text(encoding="utf-8"), True, ""
        except UnicodeDecodeError as exc:
            logger.warning("Invalid UTF-8 in %s: %s", path, exc)
            return path.read_text(encoding="utf-8", errors="replace"), False, str(exc)

    def _load_manifest(self) -> dict[str, Any]:
        payload = self._load_json(self.manifest_path)
        if not isinstance(payload, dict):
            return {}
        return payload

    @staticmethod
    def _count_paragraphs(text: str) -> int:
        text = text.replace("\r", "\n")
        return len([chunk for chunk in PARAGRAPH_RE.split(text) if chunk.strip()])

    @staticmethod
    def _count_words(text: str) -> int:
        return len(re.findall(r"\S+", text))

    @staticmethod
    def _estimate_reading_ms(text: str, wpm: int = ESTIMATE_READING_WPM) -> int:
        minutes = max(1, Pipeline._count_words(text)) / max(wpm, 1)
        return int(math.ceil(minutes * 60_000))

    def _chapter_timings_path(self, slug: str, suffix: str) -> Path:
        return self.sync_root / slug / f"{slug}_{suffix}.json"

    def _highlight_sync_path(self, slug: str) -> Path:
        return self.controlled_root / slug / "highlight_sync.json"

    def _sync_total_duration_ms(self, payload: dict[str, Any]) -> int:
        chapters = payload.get("chapters") or []
        if not isinstance(chapters, list):
            return 0
        total = 0
        for item in chapters:
            if not isinstance(item, dict):
                continue
            total += int(
                item.get("totalMs")
                or item.get("estimatedTotalMs")
                or item.get("duration_ms")
                or item.get("durationMs")
                or 0
            )
        return total

    def _backfill_enhanced_timing_from_fallback(self, slug: str, total_duration_ms: int) -> Path | None:
        enhanced_path = self._chapter_timings_path(slug, "enhanced")
        if enhanced_path.exists():
            return enhanced_path

        fallback_path = self._chapter_timings_path(slug, "fallback")
        highlight_path = self._highlight_sync_path(slug)
        fallback_payload = self._load_json(fallback_path)
        highlight_payload = self._load_json(highlight_path)
        if not isinstance(fallback_payload, dict) or not isinstance(highlight_payload, dict):
            return None

        fallback_total = self._sync_total_duration_ms(fallback_payload)
        highlight_total = self._sync_total_duration_ms(highlight_payload)
        if not fallback_total or not highlight_total:
            return None
        if abs(total_duration_ms - highlight_total) > 1000 or abs(fallback_total - highlight_total) > 1000:
            return None

        chapters = []
        for index, item in enumerate(fallback_payload.get("chapters") or [], start=1):
            if not isinstance(item, dict):
                continue
            intro_ms = int(item.get("introMs") or item.get("intro_ms") or 1500)
            outro_ms = int(item.get("outroMs") or item.get("outro_ms") or 1000)
            total_ms = int(item.get("totalMs") or item.get("estimatedTotalMs") or 0)
            content_ms = int(item.get("contentMs") or item.get("content_ms") or max(0, total_ms - intro_ms - outro_ms))
            chapters.append(
                {
                    "index": int(item.get("index") or index),
                    "chapterId": self._safe_text(item.get("chapterId") or item.get("chapter_id") or f"chapter-{index:03d}"),
                    "title": self._safe_text(item.get("title") or f"Chapter {index}"),
                    "paragraphCount": int(item.get("paragraphCount") or item.get("paragraph_count") or 0),
                    "startMs": int(item.get("startMs") or item.get("start_ms") or 0),
                    "contentMs": content_ms,
                    "introMs": intro_ms,
                    "outroMs": outro_ms,
                    "totalMs": total_ms or (content_ms + intro_ms + outro_ms),
                }
            )

        if not chapters:
            return None

        delta = total_duration_ms - sum(chapter["totalMs"] for chapter in chapters)
        if delta:
            chapters[-1]["contentMs"] = max(0, chapters[-1]["contentMs"] + delta)
            chapters[-1]["totalMs"] = chapters[-1]["contentMs"] + chapters[-1]["introMs"] + chapters[-1]["outroMs"]

        self._write_json(
            enhanced_path,
            {
                "slug": slug,
                "createdAt": self._iso_utc_now(),
                "source": "backfill_from_aligned_premium_audio",
                "chapters": chapters,
                "totalDurationMs": total_duration_ms,
            },
        )
        return enhanced_path

    def _reuse_existing_premium_audio(
        self,
        candidate: dict[str, Any],
        result: TitleResult,
        chapters: list[dict[str, Any]],
        output_path: Path,
    ) -> bool:
        if not output_path.exists():
            return False

        try:
            total_duration_ms = self._audio_duration_ms(output_path)
        except Exception as exc:
            result.add_failure(f"Existing premium audio unreadable at {output_path.relative_to(ROOT)}: {exc}")
            return False

        result.enhanced_audio_path = str(output_path.relative_to(ROOT))
        result.gate_2_enhancement = GATE_2_PASS
        result.gate2_openai_tts = False
        result.gate2_fallback = False
        result.chapter_count = len(chapters)
        result.total_paragraphs = sum(self._count_paragraphs(self._safe_text(ch.get("content"))) for ch in chapters)
        result.total_duration_ms = total_duration_ms

        enhanced_timing = self._chapter_timings_path(result.slug, "enhanced")
        if enhanced_timing.exists():
            result.chapter_timing_path = str(enhanced_timing.relative_to(ROOT))
        else:
            backfilled = self._backfill_enhanced_timing_from_fallback(result.slug, total_duration_ms)
            if backfilled is not None:
                result.chapter_timing_path = str(backfilled.relative_to(ROOT))

        logger.info("%s for %s using existing premium audio at %s", GATE_2_PASS, result.slug, output_path.relative_to(ROOT))
        return True

    async def _openai_call(self, fn):
        if self.openai_client is None:
            raise RuntimeError("OpenAI client unavailable; OPENAI_API_KEY is not configured")

        async def wrapper():
            async with self.api_sem:
                return await fn()

        try:
            return await wrapper()
        except (OpenAIError, OSError) as exc:
            raise RuntimeError(f"OpenAI API call failed: {exc}") from exc

    async def _call_director(self, text: str) -> tuple[str, dict[str, int]]:
        async def do_call():
            response = await self.openai_client.chat.completions.create(
                model=DIRECTOR_MODEL,
                temperature=0.1,
                messages=[
                    {"role": "system", "content": DIRECTOR_SYSTEM_PROMPT},
                    {"role": "user", "content": text},
                ],
            )
            usage = response.usage
            input_tokens = int(getattr(usage, "prompt_tokens", 0) or 0)
            output_tokens = int(getattr(usage, "completion_tokens", 0) or 0)
            return (
                self._safe_text(response.choices[0].message.content),
                {
                    "input": input_tokens,
                    "output": output_tokens,
                },
            )

        return await self._openai_call(do_call)

    async def _call_tts(self, text: str, voice: str, model: str = TTS_MODEL) -> bytes:
        async def do_call():
            response = await self.openai_client.audio.speech.create(
                model=model,
                voice=voice,
                input=text,
                response_format="mp3",
            )
            return await response.aread()

        return await self._openai_call(do_call)

    async def _api_with_retry(self, fn, context: str, result: TitleResult, retries: int = 1):
        last_error: Exception | None = None
        for attempt in range(retries + 1):
            try:
                return await fn()
            except Exception as exc:
                last_error = exc
                if attempt < retries:
                    result.retry_count += 1
                    logger.warning("%s attempt %s failed for %s, retrying: %s", context, attempt + 1, result.slug, exc)
                    await asyncio.sleep(0.8 * (attempt + 1))
                    continue
                raise
        if last_error is not None:
            raise last_error

    @staticmethod
    def _is_sanitized_text_valid(slug: str, text: str, source: str) -> tuple[bool, list[str]]:
        if not text:
            return False, [f"BLOCKER: no readable content in {source}"]

        issues: list[str] = []
        warnings: list[str] = []

        if "\u0000" in text:
            issues.append(f"BLOCKER: NUL byte in {source}")
        if "�" in text:
            issues.append(f"BLOCKER: replacement character in {source}")
        if re.search(r"[A-Za-z0-9]+\s*\[.*?\]\s*[A-Za-z0-9]+", text):
            warnings.append(f"possible markdown/annotation artifact in {source}")
        if re.search(r"\b(Traceback|json|Exception|Exception\w*)\b", text, flags=re.IGNORECASE):
            issues.append(f"BLOCKER: runtime/log traceback text in {source}")
        standalone_page_headers = re.findall(r"^\s*(Page\s+\d+|Chapter\s+\d+)\s*$", text, flags=re.MULTILINE | re.IGNORECASE)
        if len(standalone_page_headers) >= 3:
            issues.append(f"BLOCKER: repeated standalone page/chapter markers detected in {source} ({len(standalone_page_headers)})")

        for pattern in SUSPICIOUS_TEXT_PATTERNS:
            if pattern.search(text):
                issues.append(f"BLOCKER: suspicious token in {source}: {pattern.pattern}")

        if "..." in text and re.search(r"\.{6,}", text):
            warnings.append(f"unusual punctuation run in {source}")

        # Detect too many repetitive markers.
        odd_headers = len(re.findall(r"^\s*(?:Chapter|CHAPTER)\s+\w+\s*:?.*$", text, flags=re.MULTILINE))
        if odd_headers > 8:
            warnings.append(f"repeated chapter header-like lines in {source} ({odd_headers})")

        # Detect suspicious page markers as standalone lines repeated across body.
        standalone_digits = len(re.findall(r"^\s*\d+\s*$", text, flags=re.MULTILINE))
        if standalone_digits > 20:
            issues.append(f"BLOCKER: excessive standalone page numbers in {source}")

        digit_lines = 0
        for match in REPEATED_LINE_SYMBOL_PATTERNS[1].finditer(text):
            if match.group(0).strip():
                digit_lines += 1
        if digit_lines > 6:
            warnings.append(f"many standalone numeric lines in {source} ({digit_lines})")

        for pattern in REPEATED_LINE_SYMBOL_PATTERNS:
            if pattern is REPEATED_LINE_SYMBOL_PATTERNS[1]:
                continue
            if pattern.search(text):
                issues.append(f"BLOCKER: possible OCR artifact in {source}: repetitive symbols")

        stripped = "\n".join([ln.strip() for ln in text.splitlines() if ln.strip()])
        if not stripped:
            issues.append(f"BLOCKER: no readable text after strip for {source}")

        if "```" in text or "\n\n\n\n" in text:
            warnings.append(f"excessive spacing/markdown artifacts in {source}")

        # Heuristic header/footer duplication.
        lines = [ln.strip() for ln in text.splitlines() if ln.strip()]
        if len(lines) > 12:
            head = lines[:2]
            tail = lines[-2:]
            if head and tail and any(item in tail for item in head):
                warnings.append(f"possible repeated header/footer lines in {source}")

        if issues:
            logger.warning("Sanitization issues for %s in %s: %s", slug, source, ", ".join(issues))
            return False, issues + warnings
        return True, warnings

    def _gather_chapter_file_items(self, chapter_dir: Path) -> list[Path]:
        if not chapter_dir.exists() or not chapter_dir.is_dir():
            return []
        entries: list[Path] = []
        for path in sorted(chapter_dir.glob("*")):
            if path.is_dir():
                continue
            if path.suffix.lower() in {".md", ".txt", ".json"}:
                entries.append(path)
        return entries

    def _resolve_candidate_sidecar_path(self, candidate: dict[str, Any], key: str) -> Path | None:
        sidecars = candidate.get("assetLocations", {}).get("sidecars", {})
        source = sidecars.get(key) if isinstance(sidecars, dict) else None
        if not isinstance(source, dict):
            return None
        path_raw = self._safe_text(source.get("absolutePath") or source.get("path"))
        if not path_raw:
            return None
        candidate_path = Path(path_raw)
        if candidate_path.exists():
            return candidate_path
        fallback = ROOT / path_raw
        return fallback if fallback.exists() else None

    @staticmethod
    def _words_from_timestamps_payload(payload: Any) -> list[str]:
        if not isinstance(payload, list):
            return []
        words: list[str] = []
        for entry in payload:
            if isinstance(entry, dict):
                for key in ("word", "text", "token", "value"):
                    value = entry.get(key)
                    if value is None:
                        continue
                    text = str(value).strip()
                    if text:
                        words.append(text)
                        break
            elif isinstance(entry, str):
                text = entry.strip()
                if text:
                    words.append(text)
        return words

    def _load_chapter_items(self, slug: str, candidate: dict[str, Any]) -> list[dict[str, Any]]:
        chapters: list[dict[str, Any]] = []
        seen = set()

        candidates = [self.content_root / slug / "chapters", self.content_root / slug / "raw"]

        for chapter_dir in candidates:
            for path in self._gather_chapter_file_items(chapter_dir):
                if path in seen:
                    continue
                seen.add(path)
                payload = self._load_json(path)
                if path.suffix.lower() == ".json":
                    source = payload
                    payload_list: list[dict[str, Any]]
                    if isinstance(source, list):
                        payload_list = source
                    elif isinstance(source, dict):
                        payload_list = [source]
                    else:
                        continue
                    for item in payload_list:
                        if not isinstance(item, dict):
                            continue
                        content = self._safe_text(
                            item.get("content")
                            or item.get("text")
                            or item.get("body")
                            or item.get("value")
                        )
                        if not content:
                            continue
                        chapter_id = self._safe_text(item.get("id") or item.get("chapterId") or path.stem)
                        chapter_title = self._safe_text(item.get("title") or item.get("chapterTitle") or path.stem)
                        chapters.append(
                            {
                                "index": len(chapters) + 1,
                                "chapter_id": chapter_id,
                                "title": chapter_title,
                                "content": content,
                                "path": str(path.relative_to(ROOT)),
                            }
                        )
                else:
                    content, utf8_ok, read_msg = self._read_text_with_validation(path)
                    if not utf8_ok:
                        logger.warning("Chapter file UTF-8 issue in %s: %s", path, read_msg)
                    if not content.strip():
                        continue
                    chapters.append(
                        {
                            "index": len(chapters) + 1,
                            "chapter_id": path.stem,
                            "title": path.stem.replace("_", " "),
                            "content": content.strip(),
                            "path": str(path.relative_to(ROOT)),
                        }
                    )

        if chapters:
            return chapters

        sidecars = candidate.get("assetLocations", {}).get("sidecars", {})
        chapter_meta_path = self._resolve_candidate_sidecar_path(candidate, "chapters")
        if chapter_meta_path:
            payload = self._load_json(chapter_meta_path)
            if isinstance(payload, list):
                for item in payload:
                    if not isinstance(item, dict):
                        continue
                    content = self._safe_text(
                        item.get("content")
                        or item.get("text")
                        or item.get("body")
                        or item.get("value")
                    )
                    if not content:
                        continue
                    chapter_id = self._safe_text(item.get("id") or item.get("chapterId") or chapter_meta_path.stem)
                    chapter_title = self._safe_text(item.get("title") or item.get("chapterTitle") or chapter_meta_path.stem)
                    chapters.append(
                        {
                            "index": len(chapters) + 1,
                            "chapter_id": chapter_id,
                            "title": chapter_title,
                            "content": content,
                            "path": str(chapter_meta_path.relative_to(ROOT)),
                        }
                    )
            elif isinstance(payload, dict):
                content = self._safe_text(payload.get("content") or payload.get("text") or payload.get("body") or payload.get("value"))
                if content:
                    chapters.append(
                        {
                            "index": 1,
                            "chapter_id": self._safe_text(payload.get("id") or payload.get("chapterId") or chapter_meta_path.stem),
                            "title": self._safe_text(payload.get("title") or payload.get("chapterTitle") or chapter_meta_path.stem),
                            "content": content,
                            "path": str(chapter_meta_path.relative_to(ROOT)),
                        }
                    )

        if chapters:
            return chapters

        for timestamp_key in ("timestamps", "highlightVtt"):
            timestamp_path = self._resolve_candidate_sidecar_path(candidate, timestamp_key)
            if not timestamp_path or not timestamp_path.exists():
                continue
            words: list[str] = []
            if timestamp_path.suffix.lower() == ".json":
                words = self._words_from_timestamps_payload(self._load_json(timestamp_path))
            else:
                vtt_text = self._read_text(timestamp_path)
                for line in vtt_text.splitlines():
                    line = line.strip()
                    if not line or line == "WEBVTT" or "-->" in line or re.fullmatch(r"^\d+$", line):
                        continue
                    line = re.sub(r"<[^>]+>", "", line).strip()
                    if line:
                        words.extend(line.split())
            if words:
                text = re.sub(r"\s+", " ", " ".join(words).strip())
                chapters.append(
                    {
                        "index": 1,
                        "chapter_id": f"{slug}-chapter-1",
                        "title": self._safe_text(candidate.get("title") or slug),
                        "content": text,
                        "path": str(timestamp_path.relative_to(ROOT)),
                    }
                )
                return chapters

        return chapters

    def _chapter_source_paths(self, slug: str, candidate: dict[str, Any]) -> list[Path]:
        paths: list[Path] = []
        # Primary source in manifest candidate paths.
        source = candidate.get("assetLocations", {}).get("audio")
        if isinstance(source, list):
            for item in source:
                if not isinstance(item, dict):
                    continue
                path_candidate = self._safe_text(item.get("absolutePath"))
                if path_candidate:
                    p = Path(path_candidate)
                    if p.exists():
                        paths.append(p)
                path_rel = self._safe_text(item.get("path"))
                if path_rel:
                    p = ROOT / path_rel
                    if p.exists():
                        paths.append(p)
        elif isinstance(source, dict):
            for key in ("absolutePath", "path"):
                value = self._safe_text(source.get(key))
                if not value:
                    continue
                p = Path(value)
                if p.is_absolute():
                    if p.exists():
                        paths.append(p)
                else:
                    rp = ROOT / p
                    if rp.exists():
                        paths.append(rp)
        # Deduplicate while preserving order.
        deduped: list[Path] = []
        seen = set()
        for path in paths:
            rp = str(path.resolve())
            if rp in seen:
                continue
            seen.add(rp)
            deduped.append(path)
        return deduped

