def _utc_now() -> str:
    return datetime.now(timezone.utc).strftime("%Y-%m-%dT%H:%M:%S%z")


def _pid_alive(pid: int) -> bool:
    try:
        os.kill(pid, 0)
        return True
    except OSError:
        return False


def _acquire_run_lock(lock_path: Path, run_id: str) -> None:
    if lock_path.exists():
        try:
            payload = json.loads(lock_path.read_text(encoding="utf-8"))
            prev_pid = int(payload.get("pid") or 0)
            if prev_pid and _pid_alive(prev_pid):
                raise RuntimeError(
                    "Another audiobook production run is active with run-id="
                    f"{payload.get('run_id')} and pid={prev_pid}. "
                    "Resolve before starting a duplicate run."
                )
            logger.warning("Removing stale pipeline lock file at %s", lock_path)
            lock_path.unlink(missing_ok=True)
        except Exception:
            logger.warning("Removing stale/unreadable pipeline lock at %s", lock_path)
            lock_path.unlink(missing_ok=True)

    payload = {
        "run_id": run_id,
        "pid": os.getpid(),
        "started_at": _utc_now(),
        "command": " ".join(sys.argv),
    }
    lock_path.parent.mkdir(parents=True, exist_ok=True)
    lock_path.write_text(json.dumps(payload, ensure_ascii=False, indent=2), encoding="utf-8")


def _release_run_lock(lock_path: Path, run_id: str) -> None:
    if not lock_path.exists():
        return
    try:
        payload = json.loads(lock_path.read_text(encoding="utf-8"))
        if payload.get("run_id") == run_id:
            lock_path.unlink(missing_ok=True)
    except Exception:
        lock_path.unlink(missing_ok=True)


